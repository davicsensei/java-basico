public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
       // Inteiro (32 bits)
       int idade = 25;

       // Longo (64 bits)
       long populacaoMundial = 7800000000L;

       // Ponto flutuante (32 bits)
       float altura = 1.75f;

       // Ponto flutuante de dupla precisão (64 bits)
       double salario = 2500.50;

       // Caractere (16 bits)
       char letra = 'A';

       // Booleano (true ou false)
       boolean ehEstudante = true;

       // Byte (8 bits)
       byte codigo = 127;

       // Short (16 bits)
       short ano = 2023;

       // Exibindo os valores
       System.out.println("Idade: " + idade);
       System.out.println("População Mundial: " + populacaoMundial);
       System.out.println("Altura: " + altura);
       System.out.println("Salário: " + salario);
       System.out.println("Letra: " + letra);
       System.out.println("É estudante? " + ehEstudante);
       System.out.println("Código: " + codigo);
       System.out.println("Ano: " + ano);
   }
}
